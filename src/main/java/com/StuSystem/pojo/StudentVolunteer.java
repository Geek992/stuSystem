package com.StuSystem.pojo;

public class StudentVolunteer {
    private String stu_no;
    private String stu_name;
    private String stu_class;
    private String stu_volunteer;
    private String selection_result;

    public String getStu_no() {
        return stu_no;
    }

    public void setStu_no(String stu_no) {
        this.stu_no = stu_no;
    }

    public String getStu_name() {
        return stu_name;
    }

    public void setStu_name(String stu_name) {
        this.stu_name = stu_name;
    }

    public String getStu_class() {
        return stu_class;
    }

    public void setStu_class(String stu_class) {
        this.stu_class = stu_class;
    }

    public String getStu_volunteer() {
        return stu_volunteer;
    }

    public void setStu_volunteer(String stu_volunteer) {
        this.stu_volunteer = stu_volunteer;
    }

    public String getSelection_result() {
        return selection_result;
    }

    public void setSelection_result(String selection_result) {
        this.selection_result = selection_result;
    }
}
