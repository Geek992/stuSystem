package com.StuSystem.pojo;

public class TeacherInfo {
    private String tch_id;
    private String tch_no;
    private String tch_name;
    private String tch_pwd;
    private String tch_gender;
    private String tch_tel;
    private String tch_college;
    private String tch_photo;

    public String getTch_id() {
        return tch_id;
    }

    public void setTch_id(String tch_id) {
        this.tch_id = tch_id;
    }

    public String getTch_no() {
        return tch_no;
    }

    public void setTch_no(String tch_no) {
        this.tch_no = tch_no;
    }

    public String getTch_name() {
        return tch_name;
    }

    public void setTch_name(String tch_name) {
        this.tch_name = tch_name;
    }

    public String getTch_pwd() {
        return tch_pwd;
    }

    public void setTch_pwd(String tch_pwd) {
        this.tch_pwd = tch_pwd;
    }

    public String getTch_gender() {
        return tch_gender;
    }

    public void setTch_gender(String tch_gender) {
        this.tch_gender = tch_gender;
    }

    public String getTch_tel() {
        return tch_tel;
    }

    public void setTch_tel(String tch_tel) {
        this.tch_tel = tch_tel;
    }

    public String getTch_college() {
        return tch_college;
    }

    public void setTch_college(String tch_college) {
        this.tch_college = tch_college;
    }

    public String getTch_photo() {
        return tch_photo;
    }

    public void setTch_photo(String tch_photo) {
        this.tch_photo = tch_photo;
    }
}
